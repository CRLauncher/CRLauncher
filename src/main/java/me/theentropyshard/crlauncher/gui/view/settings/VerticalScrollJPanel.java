package me.theentropyshard.crlauncher.gui.view.settings;

import javax.swing.*;
import java.awt.*;

public class VerticalScrollJPanel extends JPanel implements Scrollable {
    public VerticalScrollJPanel(LayoutManager layout) {
        super(layout);
    }

    @Override
    public Dimension getPreferredScrollableViewportSize() {
        return this.getPreferredSize();
    }

    @Override
    public int getScrollableUnitIncrement(Rectangle visibleRect, int orientation, int direction) {
        return 2;
    }

    @Override
    public int getScrollableBlockIncrement(Rectangle visibleRect, int orientation, int direction) {
        return 10;
    }

    @Override
    public boolean getScrollableTracksViewportWidth() {
        return true;
    }

    @Override
    public boolean getScrollableTracksViewportHeight() {
        if (this.getParent() instanceof JViewport) {
            return (this.getParent().getHeight() > this.getPreferredSize().height);
        }

        return false;
    }
}