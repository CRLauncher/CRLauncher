package me.theentropyshard.crlauncher.crmm.filter;

public enum ShowPerPage {
    FIVE("five", "5"),
    TEN("ten", "10"),
    FIFTEEN("fifteen", "15"),
    TWENTY("twenty", "20"),
    FIFTY("fifty", "50"),
    ONE_HUNDRED("oneHundred", "100");

    private final String key;
    private final String value;

    ShowPerPage(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return this.key;
    }

    public String getValue() {
        return this.value;
    }
}
